CREATE TABLE SURVEYDATA(
	ID IDENTITY PRIMARY KEY, 
	STUDENTNAME VARCHAR(255), 
	STUDENTID VARCHAR(10), 
	SURVEYDATE DATE, 
	STREET VARCHAR(255), 
	CITY VARCHAR(255), 
	STATE VARCHAR(2), 
	ZIP VARCHAR(5), 
	TELNUM VARCHAR(10), 
	EMAIL VARCHAR(255),
	URL VARCHAR(255), 
	LIKED ARRAY[6], 
	HEARD VARCHAR(15), 
	GRADMON VARCHAR(15), 
	GRADYEAR VARCHAR(4), 
	RECOMMEND VARCHAR(15), 
	COMMENTS VARCHAR(255));